def time_synchronized():
    return time.time()


def update_fps(prev_time, fps, actual_fps):
    curr_time = time.time()
    if abs(curr_time - prev_time) <= 1:
        fps = fps + 1
    else:
        actual_fps = fps
        prev_time = curr_time
        fps = 0
    return prev_time, fps, actual_fps
